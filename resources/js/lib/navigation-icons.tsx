/**
 * Get an icon component by name
 * Returns a default icon if not found
 */
export function getIconComponent(iconName: string): React.ComponentType<{ className?: string }> {
  return ICON_MAP[iconName] || IconSettings;
}

/**
 * Render an icon by name with className
 */
export function renderIcon(iconName: string, className: string = 'h-5 w-5') {
  const IconComponent = getIconComponent(iconName);
  return <IconComponent className={className} />;
}
